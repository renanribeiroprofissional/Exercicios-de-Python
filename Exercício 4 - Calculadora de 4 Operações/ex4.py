# Calculadora de 4 Operações

# Escreve um programa que permita inserir dois valores numéricos e então calcule o resultado das quatro operações básicas (soma, subtração, multiplicação e divisão).

# Após calcular os resultados o programa deve exibi-los na tela.


n1 = int(input("Digite um número: "))
n2 = int(input("Digite um número: "))


print("Soma: "+ str(n1+n2))
print("Subtração: "+str(n1-n2))
print("Multiplicação: "+str(+n1*n2))
print("Divisão " +str(n1/n2))


